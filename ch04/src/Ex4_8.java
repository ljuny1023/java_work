class Ex4_8 {
	public static void main(String args[]) {
		for(int i = 1; i <= 10; i++) {	//괄호{}안의 문장을 10번 반복
			System.out.println(i);
		}
		for(int i = 10; i >= 1; i--) {	//괄호{}안의 문장을 10번 반복
			System.out.println(i);
		}
		for(int i = 1; i <= 10; i=i*2) {	//괄호{}안의 문장을 10번 반복
			System.out.println(i);
		}
	}
}